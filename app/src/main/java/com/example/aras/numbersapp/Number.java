package com.example.aras.numbersapp;

public class Number {
    int mNumber;
    int mSound;
    public Number(int mNumber, int mSound) {
        this.mNumber = mNumber;
        this.mSound = mSound;
    }

    public int getmNumber() {
        return mNumber;
    }

    public int getmSound() {
        return mSound;
    }


}
